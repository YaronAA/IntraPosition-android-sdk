package com.intraposition.buzcartsdk.models;

public class TriggersMetadata {

    private int totalMaxActs;

    private int timeBetweenActs;

    private int timeFromStart;

    public int getTotalMaxActs() {
        return totalMaxActs;
    }

    public void setTotalMaxActs(int totalMaxActs) {
        this.totalMaxActs = totalMaxActs;
    }

    public int getTimeBetweenActs() {
        return timeBetweenActs;
    }

    public void setTimeBetweenActs(int timeBetweenActs) {
        this.timeBetweenActs = timeBetweenActs;
    }

    public int getTimeFromStart() {
        return timeFromStart;
    }

    public void setTimeFromStart(int timeFromStart) {
        this.timeFromStart = timeFromStart;
    }
}
